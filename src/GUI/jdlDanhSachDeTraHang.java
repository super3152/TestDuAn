    private void btnThemphieutrahangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemphieutrahangActionPerformed
        int dongduocchon = tblDanhSachTraHang.getSelectedRow();
        if (dongduocchon < 0) {
            ThongBaoCanhBao.ThongBao("Bạn Chưa Chọn Hóa Đơn Để Trả Hàng !", "Thông Báo");
            return;
        }
        int MaHD = Integer.parseInt(tblDanhSachTraHang.getValueAt(dongduocchon, 0).toString());
        DTO.DTOHoaDon hd = BLL.BLLHoaDon.GetMaHD(MaHD);
        String SoHoaDon = tblDanhSachTraHang.getValueAt(dongduocchon, 1).toString();
        String NgayTao = tblDanhSachTraHang.getValueAt(dongduocchon, 2).toString();
        String MaNV = tblDanhSachTraHang.getValueAt(dongduocchon, 3).toString();       
        String MaKH = tblDanhSachTraHang.getValueAt(dongduocchon, 4).toString();
        String TongTien = tblDanhSachTraHang.getValueAt(dongduocchon, 5).toString();
         new pnltrahang(MaHD, SoHoaDon, NgayTao, MaNV, MaKH, TongTien).setVisible(true);
          GUI.pnlhanghoa.jPanel27.setVisible(false);
        pnltrahang tpn = new pnltrahang();
         GUI.pnlhanghoa.jPanel28.add(tpn);
         this.dispose();
          BLL.BLLTraHang.HienThiHoaDonTheoTraHang(tblDanhSachTraHang);
    }//GEN-LAST:event_btnThemphieutrahangActionPerformed

