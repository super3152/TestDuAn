    private void btnOK1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOK1ActionPerformed
        int dongDuocChon = pnltrahang.tblPhieutra.getSelectedRow();
        int MaCTHD = Integer.parseInt(pnltrahang.tblPhieutra.getValueAt(dongDuocChon, 0).toString());
        DTO.DTOChiTietHoaDon cthd = BLL.BLLTraHang.GetMaCTHD(MaCTHD);
        boolean kiemTra = false;
        int SoLuong = 0;
        double PhiTra = 0;
        PhiTra = Double.parseDouble(txtPhiTra.getText());
        SoLuong = (int) spnSoLuong.getValue();
        if (SoLuong < 1) {
            ThongBaoCanhBao.ThongBao("Số lượng trả phải lớn hơn 0", "Lỗi dữ liệu");
            kiemTra = false;
            return;
        } else if (SoLuong > cthd.getSoLuong()) {
            ThongBaoCanhBao.ThongBao("Số lượng trả của hóa đơn này tôi đa là " + cthd.getSoLuong() + "", "Thông báo");
            kiemTra = false;
            return;
        } else {

            kiemTra = true;
        }
        if (PhiTra < 0) {
            ThongBaoCanhBao.ThongBao("Phí trả phải lớn hơn 0", "Lỗi dữ liệu");
            kiemTra = false;
            return;
        } else if (PhiTra > cthd.getThanhTien()) {
            ThongBaoCanhBao.ThongBao("Phí trả không được lơn hơn giá trị đơn hàng: " + ChuyenDoi.DinhDangTien(cthd.getThanhTien()) + " VNĐ", "Thông báo");
            kiemTra = false;
            return;
        } else {

            kiemTra = true;
        }
        PhiTra = ChuyenDoi.ChuyenSangSo(txtPhiTra.getText());
        double TongTien = BLL.BLLTraHang.HienThiChiTietHoaDonSua(tblPhieutra, MaCTHD, SoLuong, PhiTra);
        double TongTienCu = BLL.ChuyenDoi.ChuyenTien(pnltrahang.txtTongtien.getText());
        double TongTienThanhToan = TongTienCu + TongTien;
        pnltrahang.txtTongtien.setText(ChuyenDoi.DinhDangTien(TongTienThanhToan));
        int SoLuongCu = Integer.parseInt(pnltrahang.txtSoLuong.getText());
        int SoLuongMoi = SoLuongCu + SoLuong;
         pnltrahang.txtSoLuong.setText(SoLuongMoi+"");
          if (pnltrahang.txtSoDonTraHang.getText().equals("")) {

            pnltrahang.txtSoDonTraHang.setText(BLL.BLLTraHang.TaoSoTraHang());

        }
        this.dispose();
    }//GEN-LAST:event_btnOK1ActionPerformed

