    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        int dongDuocChon = pnlthemphieunhap.tblChitietphieunhap.getSelectedRow();

        int MaSP = Integer.parseInt(pnlthemphieunhap.tblChitietphieunhap.getValueAt(dongDuocChon, 0).toString());

        DTOSanPham sp = BLL.BLLSanPham.GetMaSP(MaSP);

        boolean kiemTra = false;
        int soLuong = 0;

        soLuong = (int) spnSoLuong.getValue();
        if (soLuong < 1) {
            ThongBaoCanhBao.ThongBao("Số lượng nhập phải lớn hơn 0", "Lỗi dữ liệu");
            kiemTra = false;
            return;
        } else {

            kiemTra = true;
        }

        double GiaNhap =ChuyenDoi.ChuyenSangSo(txtGiaSanPham.getText());
        int slmoi = Integer.parseInt(spnSoLuong.getValue().toString());

        double tongTien = BLL.BLLPhieuNhap.SuaSanPhamVaoPhieuNhap(pnlthemphieunhap.tblChitietphieunhap, sp, slmoi, GiaNhap);
        double TongTienDau = ChuyenDoi.ChuyenSangSo(GUI.pnlthemphieunhap.txtTongtien.getText());
        double TongTien = TongTienDau + tongTien;
        GUI.pnlthemphieunhap.txtTongtien.setText(BLL.ChuyenDoi.DinhDangTien(TongTien));
        this.dispose();
    }//GEN-LAST:event_btnOKActionPerformed

